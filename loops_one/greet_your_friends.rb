# Your friends just showed up! Given the following array of names, use a for loop to greet each friend individually.

friends = ['Sarah', 'John', 'Hannah', 'Dave']

for name in friends do
  puts "Hello, #{name}!"
end

=begin
When naming variables in a for loop it's typical to use the standard format: for friend in friends, for cat in cats,
etc. This makes it clear that we're iterating over friends and doing something with each friend.

=end

